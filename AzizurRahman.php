        <h2 class="w3-text-grey w3-padding-16"><i class="fa fa-certificate fa-fw w3-margin-right w3-xxlarge w3-text-teal"></i>Design and Optimisation of New Generation of Compact Photonic Sensors </h2>
        <h4 class="w3-text-grey w3-padding-16">Abstract</h4>
        <div class="w3-container">
          
<p>Although optical sensors using side polished or etched optical fibres, metal coated for plasmonic resonances, and incorporating Bragg grating are now sufficiently mature and well established in the market, however, designs based on more exotic nanowires and photonic crystal fibres are becoming increasingly important and showing much improved sensitivity by accessing a larger evanescent field. Similarly, novel planar design concepts, such as the silicon slot guide-based design is showing even greater promise, allowing the exploitation of well-developed CMOS fabrication technologies for potentially low-cost sensor elements. In compact Integrated Optic format, dielectric slots, plasmonic slots, Mach-Zehnder interferometer, and ring resonators are also emerging as potential optical sensors of the future.
However, high index contrast also makes the modes in such guided-wave structures fully hybrid in nature and in such a case, full-vectorial rigorous numerical approaches will be necessary for their design optimisation. Some selected results for silicon based compact photonic sensors will be presented illustrating the value and potential of the computationally efficient finite element method in such designs.  
The development of slot waveguides has created new opportunities for exotic optical sensor by guiding light in low index sensing area, such as gases or fluids in the slot region and thus creating the potential for more advanced, sensitive yet very compact and low-cost photonic sensors. The design and optimisation of such novel vertical slots, horizontal slots and polarization independent cross-slots for biosensing will be presented. The design of a novel plasmonic slot supporting complex plasmonic modes for gas sensing will also be presented. The design optimisation of metal coated silica nanowire for bio sensing and doped ZnO coated gold nanowire and vertical slot for gas sensing and a resonating straight slots for biosensing will also be presented.
</p>
